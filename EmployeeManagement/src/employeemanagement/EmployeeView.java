    public void open() {
        JFileChooser jfc = new JFileChooser();
        int mychoose = jfc.showSaveDialog(jfc);
        if (mychoose == JFileChooser.APPROVE_OPTION) {
            String myfile = jfc.getSelectedFile().getAbsolutePath();
            try {
                FileInputStream fis = new FileInputStream(myfile);
                ObjectInputStream ois = new ObjectInputStream(fis);
                list = (List<Employee>) ois.readObject();
                filltotable();
                tblnhanvien.setRowSelectionInterval(0, 0);
                showdetail();
            } catch (Exception e) {
                clear();
            }
        }

    }

    public void exit() {
        JFileChooser jfc = new JFileChooser();
        int mychoose = jfc.showSaveDialog(jfc);
        if (mychoose == jfc.APPROVE_OPTION) {
            String myfile = jfc.getSelectedFile().getAbsolutePath();
            try {
                ObjectOutputStream oos = new ObjectOutputStream(new FileOutputStream(myfile));
                oos.writeObject(list);
                System.exit(0);
            } catch (Exception e) {
            }
        }
    }

    public void add() {
        if (check()) {
            list.add(new Employee(txtma.getText(), txtten.getText(), Integer.parseInt(txttuoi.getText()), txtemail.getText(), Double.parseDouble(txtluong.getText())));
            filltotable();
            tblnhanvien.setRowSelectionInterval(list.size() - 1, list.size() - 1);
            showdetail();
        }
    }

    public void delete() {
        index = tblnhanvien.getSelectedRow();
        try {
            if (index >= 0) {
                int d = JOptionPane.showConfirmDialog(this, "Bạn Chắc Chắn Muốn Xóa Nhân Viên " + txtma.getText());
                if (d == 0) {
                    list.remove(index);
                    clear();
                    filltotable();
                }
            }
        } catch (Exception e) {
        }
    }

    public void find() {
        String manv = JOptionPane.showInputDialog("Nhập mã nhân viên");
        boolean kq = false;
        for (Employee e : list) {
            if (e.getManv().equalsIgnoreCase(manv)) {
                kq = true;
                index = list.indexOf(e);
                tblnhanvien.setRowSelectionInterval(index, index);
                showdetail();
            }
        }
        if (!kq) {
            JOptionPane.showMessageDialog(this, "Không có nhân viên nào mã " + manv);
        }
    }

    public void update() {
        index = tblnhanvien.getSelectedRow();
        if (check()) {
            list.set(index, new Employee(txtma.getText(), txtten.getText(), Integer.parseInt(txttuoi.getText()), txtemail.getText(), Double.parseDouble(txtluong.getText())));
            filltotable();
            tblnhanvien.setRowSelectionInterval(index, index);
            showdetail();
        }
    }

    public void next() {
        try {
            index++;
            if (index > list.size() - 1) {
                index = 0;
            }
            tblnhanvien.setRowSelectionInterval(index, index);
            showdetail();
        } catch (Exception e) {
        }
    }

    public void prev() {
        try {
            index--;
            if (index < 0) {
                index = list.size() - 1;
            }
            tblnhanvien.setRowSelectionInterval(index, index);
            showdetail();
        } catch (Exception e) {
        }
    }

    public void first() {
        try {
            index = 0;
            tblnhanvien.setRowSelectionInterval(0, 0);
            showdetail();
        } catch (Exception e) {
        }
    }

    public void last() {
        try {
            index = list.size() - 1;
            tblnhanvien.setRowSelectionInterval(index, index);
        } catch (Exception e) {
        }
    }
