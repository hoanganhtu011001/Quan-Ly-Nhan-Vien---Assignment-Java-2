    public void mytimer() {
        Thread t = new Thread() {
            SimpleDateFormat sdf = new SimpleDateFormat("hh:mm aa");

            public void run() {
                while (true) {
                    Date d = new Date();
                    lbltimer.setText(sdf.format(d));
                    try {
                        Thread.sleep(1000);
                    } catch (InterruptedException ex) {
                        break;
                    }
                }
            }
        };
        t.start();
    }

    public void showdetail() {
        index = tblnhanvien.getSelectedRow();
        Employee e = list.get(index);
        txtma.setText(e.getManv());
        txtten.setText(e.getHoten());
        txttuoi.setText(String.valueOf(e.getTuoi()));
        txtemail.setText(e.getEmail());
        txtluong.setText(String.valueOf(e.getLuong()));
        lblindex.setText("Record: " + (String.valueOf(index + 1)) + " Of " + String.valueOf(list.size()));
    }

    public void filltotable() {
        DefaultTableModel mymodel = (DefaultTableModel) tblnhanvien.getModel();
        mymodel.setRowCount(0);
        for (Employee e : list) {
            mymodel.addRow(new Object[]{e.getManv(), e.getHoten(), e.getTuoi(), e.getEmail(), e.getLuong()});
        }
    }

    public boolean check() {
        if (txtma.getText().length() == 0) {
            JOptionPane.showMessageDialog(this, "Không để trống mã nhân viên");
            txtma.requestFocus();
            return false;
        }
        if (txtten.getText().length() == 0) {
            JOptionPane.showMessageDialog(this, "Không để trống họ và tên");
            txtten.requestFocus();
            return false;
        }
        if (txttuoi.getText().length() == 0) {
            JOptionPane.showMessageDialog(this, "Không để trống tuổi");
            txttuoi.requestFocus();
            return false;
        }
        try {
            if (Integer.parseInt(txttuoi.getText()) < 0) {
                JOptionPane.showMessageDialog(this, "Tuổi phải lớn hơn 0");
                txttuoi.requestFocus();
                return false;
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Tuổi phải là số");
            txttuoi.requestFocus();
            return false;
        }
        if (txtemail.getText().length() == 0) {
            JOptionPane.showMessageDialog(this, "Không để trống email");
            txtemail.requestFocus();
            return false;
        }
        if (txtluong.getText().length() == 0) {
            JOptionPane.showMessageDialog(this, "Không để trống lương");
            txtluong.requestFocus();
            return false;
        }
        try {
            if (Double.parseDouble(txtluong.getText()) < 0) {
                JOptionPane.showMessageDialog(this, "Lương phải lớn hơn 0");
                txtluong.requestFocus();
                return false;
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Lương phải là số");
            txtluong.requestFocus();
            return false;
        }
        return true;
    }

